export const getPieceTable = (pieceType: PieceTypes, isEndgame: boolean): number[][] => {
    if (pieceType === PieceTypes.PAWN) {
        return PAWN_TABLE;
    }
    if (pieceType === PieceTypes.KNIGHT) {
        return KNIGHT_TABLE;
    }
    if (pieceType === PieceTypes.BISHOP) {
        return BISHOP_TABLE;
    }
    if (pieceType === PieceTypes.ROOK) {
        return ROOK_TABLE;
    }
    if (pieceType === PieceTypes.QUEEN) {
        return QUEEN_TABLE;
    }
    if (isEndgame) {
        return KING_END_TABLE;
    }
    return KING_MID_TABLE;
}
